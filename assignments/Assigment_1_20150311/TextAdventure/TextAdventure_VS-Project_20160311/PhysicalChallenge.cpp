#include "PhysicalChallenge.h"

PhysicalChallenge::PhysicalChallenge(StringOO description, int difficulty)
{
	m_description = description;
	m_difficulty = difficulty;
	m_type = "physical";
}

PhysicalChallenge::~PhysicalChallenge()
{
}
/*******************************************************************************************************
*
*******************************************************************************************************/
const char * PhysicalChallenge::GetDescription()
{
	return m_description.GetString();
}
/*******************************************************************************************************
*
*******************************************************************************************************/
const int PhysicalChallenge::GetDifficulty()
{
	return m_difficulty;
}
/*******************************************************************************************************
*
*******************************************************************************************************/
const char * PhysicalChallenge::GetType()
{
	return m_type;
}
/*******************************************************************************************************
*
*******************************************************************************************************/
int PhysicalChallenge::GetEnemy()
{
	return m_enemy;
}
/*******************************************************************************************************
*
*******************************************************************************************************/
void PhysicalChallenge::SetDifficulty(int difficulty)
{
	m_difficulty = difficulty;
}
/*******************************************************************************************************
*
*******************************************************************************************************/
void PhysicalChallenge::SetEnemy(int enemy)
{
	m_enemy = enemy;
}