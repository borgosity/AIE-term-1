#include "StarTrooper.h"



/******************************************************************************
*******************************************************************************/
StarTrooper::StarTrooper()
{
}

/******************************************************************************
*******************************************************************************/
StarTrooper::~StarTrooper()
{
}

/******************************************************************************
*******************************************************************************/
bool StarTrooper::isAlive() const
{
	return m_health > 10.0f;
}

/******************************************************************************
*******************************************************************************/
void StarTrooper::takeHitFrom(const Weapon *weapon)
{
	if (weapon != 0)
	{
		m_health -= weapon->getDamage();
		if (m_health < 0)
		{
			m_health = 0;
		}
	}
}
