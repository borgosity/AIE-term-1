#include "Player.h"


/******************************************************************************
*******************************************************************************/
Player::Player()
{
}


/******************************************************************************
*******************************************************************************/
Player::~Player()
{
}

/******************************************************************************
*******************************************************************************/
bool Player::isAlive() const
{
	return m_health > 25.0f;
}

/******************************************************************************
*******************************************************************************/
void Player::takeHitFrom(const Weapon *weapon)
{
	if (weapon != 0)
	{
		m_health -= weapon->getDamage();
		if (m_health < 0)
		{
			m_health = 0;
		}
	}
}
